def _autolink(text: str) -> str:
    def _wrap(m):
        s = m.group(1)
        href = s
        if s.startswith('www.'):
            href = 'https://' + s
        if '@' in s and not s.startswith(('http://', 'https://', 'www.')):
            href = 'mailto:' + s
        return f'<a href="{href}">{s}</a>'
    return _LINK_RX.sub(_wrap, text or '')

